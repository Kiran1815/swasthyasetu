// ── Translation Helper ───────────────────────────
// t('key')           → returns translated string
// t('key', {x:'v'})  → replaces {x} with v
function t(key, params) {
  const lang = (AppState && AppState.currentLang) || 'en';
  let str = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key])
         || (TRANSLATIONS.en    && TRANSLATIONS.en[key])
         || key;
  if (params) {
    Object.keys(params).forEach(k => {
      str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), params[k]);
    });
  }
  return str;
}

// ── Apply Translations to Static HTML ────────────
// Looks for [data-i18n] attributes and replaces
// textContent (or innerHTML if key contains <br>)
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val.includes('<br>')) {
      el.innerHTML = val;
    } else {
      el.textContent = val;
    }
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });

  // Update HTML lang attribute
  const lang = (AppState && AppState.currentLang) || 'en';
  document.documentElement.lang = lang;
}
